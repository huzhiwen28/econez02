//CRC校验的函数
u16 CRC16(unsigned char *puchMsg, unsigned short usDataLen)
{
	unsigned char uchCRCHi = 0xFF ; /* 高CRC字节初始化 */
	unsigned char uchCRCLo = 0xFF ; /* 低CRC 字节初始化 */
	unsigned uIndex ; /* CRC循环中的索引 */
	while (usDataLen--) /* 传输消息缓冲区 */
	{
		uIndex = uchCRCHi ^ *puchMsg++ ; /* 计算CRC */
		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex] ;
		uchCRCLo = auchCRCLo[uIndex] ;
	}
	return (uchCRCHi << 8 | uchCRCLo) ;
}

/*
com初始化
*/
void C485COM_Init(void)
{
	//状态初始化
	C485COM_Backend.state = 0;
	C485COM_Backend.buffend = 0;
	C485COM_Backend.allcnt = 0;
}

//打开设备
void C485COM_Open(void)
{
    C485COM_Backend.comopen = 1;
}

//关闭设备
void C485COM_Close(void)
{
	if (C485COM_Backend.comopen == 1)
	{
		C485COM_Backend.comopen = 0;
	}
}

//任务执行
void C485COM_Run(void *p_arg)
{
    C485COM_Init();
	C485COM_Open();

    while(1)
	{
	//只要串口打开，则后台运行
	if(C485COM_Backend.comopen )
	{
#ifdef TEST485
	    u8 buff[30];
		u16 l = 0;
		if(BSP_485InBuffLen()>0)
		{
			l = BSP_485Read(buff,BSP_485InBuffLen());
			BSP_485Write(buff,l);
		}
#else

		int cnt = 0;
		
		//查询校验结果
		if(App_Encryption_Invalid() == 1)
		{
			OSTimeDlyHMSM(0, 0, 0, 1);
			continue;
		}

		//空闲态
		if(C485COM_Backend.state == 0)
		{
		  //收到字符
		  if((cnt = BSP_485InBuffLen()) > 0)
		  {
			//3.5T定时器
		    StartTimer(5);
			C485COM_Backend.state = 1;

			BSP_485Read(C485COM_Backend.buff,cnt);

			C485COM_Backend.buffend = cnt;
		  }
		}

		//接收态
		if(C485COM_Backend.state == 1)
		{
		  //帧分隔时间到
		  if(CheckTimer() == TRUE)
		  {
		     C485COM_Backend.state = 2;
		  }
		  //收到字符
		  else if((cnt = BSP_485InBuffLen()) > 0)
		  {
			//3.5T定时器
		    StartTimer(5);

			//数据过长，只取一共1024个数据(含原来的数据)
			if(cnt+C485COM_Backend.buffend > 1024)
			{
				//实际读的数据长度，否则溢出
				BSP_485Read(C485COM_Backend.buff + C485COM_Backend.buffend,(1024-C485COM_Backend.buffend));

				C485COM_Backend.buffend = 1024;
			}
			else
			{
				//没有溢出，全部读
				BSP_485Read(C485COM_Backend.buff +  C485COM_Backend.buffend,cnt);

				C485COM_Backend.buffend = cnt + C485COM_Backend.buffend;
			}
		  }
		}

		//处理态
		if(C485COM_Backend.state == 2)
		{
		   //目标为自己
		   if(C485COM_Backend.buff[0] == MODBUSID)
		   {

			  //根据功能码发送给不同的任务处理
			  switch(C485COM_Backend.buff[1])
			  {
			  	//读控制器数据
			  case 0x03:
			  	//写控制器数据,单个
			  case 0x06:
			  	//写控制器数据
			  case 0x10:

					C485_ModbusSlave_ProcessFrame(C485COM_Backend.buff,C485COM_Backend.buffend);
					break;

			  default:
				  ;
			  }
		   }
		   //清理buff和串口buff
		   C485COM_Backend.buffend = 0;
		   C485COM_Backend.state = 0;
		   BSP_485ClearReadBuff();
		}
#endif		

	}

	OSTimeDlyHMSM(0, 0, 0, 1);
	}
}


//写字符
int C485COM_Write(u8* chardata,int len)
{
	if(C485COM_Backend.comopen)
	{
		BSP_485Write(chardata, len);
	}
	return len;
}


