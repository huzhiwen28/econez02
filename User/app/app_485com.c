//串口的后台线程
struct _C485COM_Backend
{
	//缓冲
	u8 buff[1024];

	//空闲位置，指向数据后面的空白
	int buffend;

	//状态机 0空闲 1接收 2处理
	unsigned char state;

	//设备是否打开
	char comopen;

	int allcnt;

	//待检查时间
	u32 tickstime;
	//时间记录
	u32 oldtickstime;

}C485COM_Backend;

typedef enum {FALSE = 0, TRUE = !FALSE} bool;

static void StartTimer(unsigned int time)
{
	C485COM_Backend.tickstime = (OS_TICKS_PER_SEC/1000) * time;
	C485COM_Backend.oldtickstime = OSTimeGet();
}

static bool CheckTimer()
{
	if((OSTimeGet() - C485COM_Backend.oldtickstime) > C485COM_Backend.tickstime)
	{
		return TRUE;
	}
	else
	{
		return FALSE;
	}
}

